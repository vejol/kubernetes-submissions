const { Pool } = require("pg")

const pool = new Pool({
  user: process.env.POSTGRES_USER,
  host: process.env.POSTGRES_HOST,
  database: process.env.POSTGRES_DATABASE,
  password: process.env.POSTGRES_PASSWORD,
  port: process.env.POSTGRES_PORT,
})

const initializeDb = async () => {
  try {
    await pool.query(`
      CREATE TABLE IF NOT EXISTS todos (
        id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        content TEXT NOT NULL,
        done BOOLEAN NOT NULL DEFAULT FALSE
      )
    `)

    await pool.query(
      `ALTER TABLE todos ADD COLUMN IF NOT EXISTS done BOOLEAN DEFAULT FALSE`,
    )

    console.log("Postgres database is ready to operate")
  } catch (err) {
    throw err
  }
}

const getTodos = async () => {
  try {
    const result = await pool.query("SELECT * FROM todos")
    return result.rows
  } catch (err) {
    console.error("Error while fetching todos from database", err.stack)
    throw err
  }
}

const addTodo = async content => {
  try {
    const result = await pool.query(
      "INSERT INTO todos (content) VALUES ($1) RETURNING *",
      [content],
    )
    return result.rows[0]
  } catch (err) {
    console.error("Error while adding todo to database", err.stack)
    throw err
  }
}

const setTodoDone = async id => {
  try {
    const result = await pool.query(
      "UPDATE todos SET done = TRUE WHERE id = $1 RETURNING *",
      [id],
    )

    if (result.rowCount === 0) {
      throw new Error(`Todo with id ${id} not found`)
    }

    return result.rows[0]
  } catch (err) {
    console.error("Error while marking todo as done", err.stack)
    throw err
  }
}

module.exports = { addTodo, initializeDb, getTodos, setTodoDone, pool }
